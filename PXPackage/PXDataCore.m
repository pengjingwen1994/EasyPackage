@implementation PXDataCore

+ (NSArray *)expressServiceCompanyArray{

	return [[[PXDataCore expressServiceDictionary] allKeys]
			sortedArrayUsingComparator:^NSComparisonResult(id obj1, id obj2) {

				if ([obj1 compare:obj2 options:NSCaseInsensitiveSearch] == NSOrderedDescending) {
					return NSOrderedDescending;
				}
				return NSOrderedAscending;
			}];
}

+ (NSArray *)expressServiceCompanyNameArray{

	NSMutableArray *companyNameArray = [[NSMutableArray alloc] init];

	NSDictionary *serviceDictionary = [PXDataCore expressServiceDictionary];
	for (NSString *company in [PXDataCore expressServiceCompanyArray]) {
		[companyNameArray addObject:[[serviceDictionary valueForKey:company] valueForKey:@"companyname"]];
	}
	return [NSArray arrayWithArray:companyNameArray];
}

